// Helper functions
export function getProductsByMovement(movement: MovementType): Product[] {
  return products.filter((p) => p.movements.includes(movement));
}

export function getProductsByCategory(category: Category): Product[] {
  return products.filter((p) => p.category === category);
}

export function getBestSellers(): Product[] {
  return products.filter((p) => p.isBestSeller);
}

export function getNewArrivals(): Product[] {
  return products.filter((p) => p.isNew);
}

export function getProductById(id: string): Product | undefined {
  return products.find((p) => p.id === id);
}

export function getMovementById(id: MovementType): Movement | undefined {
  return movements.find((m) => m.id === id);
}

export function getArticlesByMovement(movement: MovementType): Article[] {
  return articles.filter((a) => a.relatedMovements.includes(movement));
}

export function getArticlesByCategory(category: string): Article[] {
  return articles.filter((a) => a.category === category);
}

export function getBundlesByMovement(movement: MovementType): Bundle[] {
  return bundles.filter((b) => b.movement === movement);
}
